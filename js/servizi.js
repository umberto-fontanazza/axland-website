/* =============================== COMMERCIAL & PRODOTTO ========================================= */

const btnCom = document.querySelector(".btnServiziCom");
const imgCom = document.querySelector(".imgServiziCom");
const ixCom = document.querySelector(".ixCom");
const textCom = document.querySelector(".textServiziCom");
const titleCom = document.querySelector(".titleCom");

btnCom.addEventListener("click", () => {
  btnCom.classList.add("addDisplayNone");
  imgCom.classList.add("addFilterServizi");
  ixCom.classList.add("addDisplayBlock");
  textCom.classList.add("addDisplayBlock");
  titleCom.classList.add("addDisplayNone");
});

ixCom.addEventListener("click", () => {
  btnCom.classList.remove("addDisplayNone");
  imgCom.classList.remove("addFilterServizi");
  ixCom.classList.remove("addDisplayBlock");
  textCom.classList.remove("addDisplayBlock");
  titleCom.classList.remove("addDisplayNone");
});

